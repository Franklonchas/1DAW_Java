    private DecimalFormat f = new DecimalFormat("0.00");

    public void conversor(String Moneda) {
        try {
            switch (Moneda) {
                case "euro": {
                    float cantidad = Float.parseFloat(euro.getText());
                    dolar.setText("" + f.format(cantidad * 1.23));
                    libra.setText("" + f.format(cantidad * 0.88));
                    break;
                }
                case "dolar": {
                    float cantidad = Float.parseFloat(dolar.getText());
                    euro.setText("" + f.format(cantidad * 0.81));
                    libra.setText("" + f.format(cantidad * 0.71));
                    break;
                }
                case "libra": {
                    float cantidad = Float.parseFloat(libra.getText());
                    euro.setText("" + f.format(cantidad * 1.13));
                    dolar.setText("" + f.format(cantidad * 1.39));
                    break;
                }
            }

        } catch (NumberFormatException n) {
            if ("".equals(euro.getText())) {
                libra.setText(null);
                dolar.setText(null);
            }
            if ("".equals(euro.getText())) {
                libra.setText(null);
                dolar.setText(null);
            }
            if ("".equals(euro.getText())) {
                libra.setText(null);
                dolar.setText(null);
            } else {
                JOptionPane.showMessageDialog(null, "Solo se pueden introducir n√∫meros", "Error campos", JOptionPane.ERROR_MESSAGE);
                limpiar();
            }
        }
    }

    public void limpiar() {
        euro.setText(null);
        dolar.setText(null);
        libra.setText(null);
    }

