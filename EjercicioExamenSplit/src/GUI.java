    public void analizarFrase() {
        int cont = 0;
        String frase = frase1.getText();
        String exclusor = choice1.getSelectedItem();

        if (exclusor == "Punto") {
            String[] nuevaCadena = frase.split("\\.");
            for (int i = 0; i < nuevaCadena.length; i++) {
                frase2.append(nuevaCadena[i] + '\n');
                cont++;
            }
            numerop.setText("" + cont);
        } else if (exclusor == "Punto y coma") {
            String[] nuevaCadena = frase.split("\\;");
            for (int i = 0; i < nuevaCadena.length; i++) {
                frase2.append(nuevaCadena[i] + '\n');
                cont++;
            }
            numerop.setText("" + cont);

        } else if (exclusor == "Coma") {
            String[] nuevaCadena = frase.split("\\,");
            for (int i = 0; i < nuevaCadena.length; i++) {
                frase2.append(nuevaCadena[i] + '\n');
                cont++;
            }
            numerop.setText("" + cont);

        } else if (exclusor == "Espacio en blanco") {
            String[] nuevaCadena = frase.split("\\ ");
            for (int i = 0; i < nuevaCadena.length; i++) {
                frase2.append(nuevaCadena[i] + '\n');
                cont++;
            }
            numerop.setText("" + cont);
        }
    }
    
    public void nuevaFrase(){
        frase1.setText("");
        frase2.setText("");
        numerop.setText("");
        choice1.select("Espacio en blanco");
    }

