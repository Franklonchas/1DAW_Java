    public void nuevaColumna() {
        String var;
        var = textfield.getText();
        if (!"".equals(var)) {
            modelo.addColumn(var);
        } else {
            System.err.println("No se puede añadir una columna sin nombre");
        }
    }

    public void añadirFila() {
        int numColumnas = modelo.getColumnCount();
        Object[] ob = new Object[numColumnas];
        modelo.addRow(ob);
        tabla.setModel(modelo);
    }

