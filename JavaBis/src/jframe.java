    public void eliminarFila() {
        try {
            modelo.removeRow(tabla.getSelectedRow());
        } catch (ArrayIndexOutOfBoundsException evt) {
            System.err.println("Que intentas borar?!?!");
        }
    }

    public void limpiarFilas() {
        try {
            int filas = tabla.getRowCount();
            for (int i = 0; filas > i; i++) {
                modelo.removeRow(0);
            }
        } catch (Exception evt) {
            System.err.println("Que intentas borar!?!?");
        }
    }

    public void cambiarNombre() {
        modelo2 = new DefaultTableModel();
        int ncol = modelo.getColumnCount();

        for (int i = 1; i <= ncol; i++) {
            modelo2.addColumn("Columna " + i);
        }
        Object[] fila = new Object[ncol];
        modelo2.addRow(fila);
        for (int i = 0; i < ncol; i++) {
            modelo2.setValueAt(modelo.getColumnName(i), 0, i);
        }
        tabla2.setModel(modelo2);
    }

    public void cambiarIdentificadores() {
        dialogo.pack();
        Vector identif = new Vector();
        for (int i = 0; i < modelo2.getColumnCount(); i++) {
            identif.add(tabla2.getValueAt(0, i));
        }
        modelo.setColumnIdentifiers(identif);
    }

    public void borrarColumna() {
        try {
            modelo.setColumnCount(modelo.getColumnCount() - 1);
        } catch (ArrayIndexOutOfBoundsException evt) {
            System.err.println("No puedes borrar colunmas que NO existen!!!");
        }
    }

