    public void generar() {
        int aux = Integer.parseInt(simulaciones.getText());
        int aux2 = 0;
        
        if (simular.isSelected() == true) {
            Bombo bombo = new Bombo(49);
            for (int i = 0; i < 6; i++) {
                lista1.add(i, bombo.sacaBola() + 1);
            }
            lista1.sort(null);
            
            for (int k = 0; k < lista1.size(); k++) {
                for (int i = 0; i < tabla2.getColumnCount(); i++) {
                    for (int j = 0; j < tabla2.getRowCount(); j++) {
                        tabla1.setValueAt(lista1.get(k), j, i);
                        lista1.remove(k);
                    }
                }
            }
        }
        if (generar.isSelected() == true) {
            for (int x = 0; x < aux; x++) {
                Bombo bombo = new Bombo(49);
                for (int i = 0; i < 6; i++) {
                    lista2.add(i, bombo.sacaBola() + 1);
                }
                lista2.sort(null);
                
                for (int k = 0; k < lista2.size(); k++) {
                    for (int i = 0; i < tabla2.getColumnCount(); i++) {
                        for (int j = 0; j < tabla2.getRowCount(); j++) {
                            tabla1.setValueAt(lista2.get(k), j, i);
                            if (lista1 == lista2) {
                                aux2++;
                            }
                            lista2.remove(k);
                        }
                    }
                }
            }
        }
        casos.setText("" + aux2);
    }

