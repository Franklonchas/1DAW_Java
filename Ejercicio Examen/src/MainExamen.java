    public void Analizar(){
        String textoInicial = jTextArea1.getText();
 
    switch (choice1.getSelectedItem())
     { 
         case "Punto": textoAnalizado = textoInicial.split("[.]");;  break;
     case "Punto y coma":  textoAnalizado = textoInicial.split("[;]"); break;
     case "Coma":    textoAnalizado = textoInicial.split("[,]");; break; 
     case "Espacio en blanco": textoAnalizado = textoInicial.split(" "); break; 
  
      } 
      
          for (int i=0;i<textoAnalizado.length;i++){
            jTextArea2.append(textoAnalizado[i] +  "\n" );
          }
         String textoFinal= String.valueOf(textoAnalizado.length);
         textField1.setText(textoFinal);
        
    }

