    public void generaTabla(){
        modeloDatos = new DefaultTableModel();
        modeloDatos.addColumn("Nombre");
        modeloDatos.addColumn("Direccion");
        modeloDatos.addColumn("Telefonos");
        modeloDatos.addColumn("Localidad");
        modeloDatos.addColumn("Email");
        modeloDatos.addColumn("Edad");
        tabla.setModel(modeloDatos);
    }
    
    

    public void rellenarListaDesplegable() {
        ResultSet res = agenda.obtenerRegistros("contacto");
        if (res != null) {
            combo.removeAll();
            try {
                while (res.next()) {
                    combo.addItem(res.getString("nombre"));
                }
            } catch (SQLException evt) {
                System.err.println("meh");
            }
        }
    }

    public void mostrarRegistro() {
        String nom = "" + combo.getSelectedItem();
        modelo contacto = agenda.obtenerContacto(nom);
        if (contacto != null) {
            jnombre.setText(contacto.getNombre());
            jdireccion.setText(contacto.getDireccion());
            jedad.setText("" + contacto.getEdad());
            jnumeros.setText(contacto.getTelefono());
            jlocalidad.setText(contacto.getLocalidad());
            jemail.setText(contacto.getEmail());
        }
    }

    public void leerDatos() {
        String nom = jnombre.getText();
        String dir = jdireccion.getText();
        int edad = Integer.parseInt(jedad.getText());
        String num = jnumeros.getText();
        String loc = jlocalidad.getText();
        String email = jemail.getText();
        try {
            edad = Integer.parseInt(jedad.getText());
        } catch (NumberFormatException evt) {
        }
        contacto = new modelo(nom, dir, num, loc, email, edad);
    }

    public void limpiarCampos() {
        jnombre.setText(null);
        jdireccion.setText(null);
        jedad.setText(null);
        jnumeros.setText(null);
        jlocalidad.setText(null);
        jemail.setText(null);

        jnombre.setRequestFocusEnabled(true);

    }

    public void modificar() {
        leerDatos();
        agenda.modificarContacto(contacto);
        limpiarCampos();
    }

    public void borrar() {
        String nom = jnombre.getText();
        agenda.borrarContacto(nom);
        limpiarCampos();
        rellenarListaDesplegable();
    }

    public void grabar() {
        String nom = jnombre.getText();
        contacto = agenda.obtenerContacto(nom);
        if (contacto == null) {
            nuevoContacto();
        } else {
            modificarContacto();
        }
    }

    public void nuevoContacto() {
        leerDatos();
        agenda.nuevoContacto(contacto);
        rellenarListaDesplegable();
        limpiarCampos();
    }

    public void modificarContacto() {
        leerDatos();
        agenda.modificarContacto(contacto);
        limpiarCampos();
    }
    
    public void agregarFila() {
        Object[] fila = new Object[modeloDatos.getColumnCount()];
        modeloDatos.addRow(fila);
    }

    public void visualizarContactos() {
        ResultSet res = agenda.obtenerRegistros("contacto");
        int fila = 0;
        if (res != null) {
            try{
                while(res.next()){
                agregarFila();
                modeloDatos.setValueAt(res.getString("nombre"), fila, 0);
                modeloDatos.setValueAt(res.getString("direccion"), fila, 1);
                modeloDatos.setValueAt(res.getString("telefonos"), fila, 2);
                modeloDatos.setValueAt(res.getString("localidad"), fila, 3);
                modeloDatos.setValueAt(res.getString("correo"), fila, 4);
                modeloDatos.setValueAt(res.getString("edad"), fila++, 5);
                }
            }catch(SQLException evt){}
        }
    }

